# 1. Simple lambda function
square = lambda x: x ** 2
print(square(5))  # Expected output: 25

# 2. Lambda function for multiplying two numbers
multiply = lambda x, y: x * y
print(multiply(4, 5))  # Expected output: 20

# 3. Lambda function for subtraction
subtract = lambda x, y: x - y
print(subtract(10, 4))  # Expected output: 6

# 4. Lambda function for checking if a number is even
is_even = lambda x: x % 2 == 0
print(is_even(4))  # Expected output: True

# 5. Lambda function for getting the first character of a string
get_first_char = lambda s: s[0]
print(get_first_char("Hello"))  # Expected output: H
